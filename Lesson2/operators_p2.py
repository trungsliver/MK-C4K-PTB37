# Operators with Strings
    # Concatenation:
firstName = 'Hai Nam'
lastName = 'Ho'
fullName = firstName + ' ' + lastName
print(fullName)

    # Repetition:
print('hi ' * 3)
print('hi ' * 0)

# Comparison Operators (Toán tử quan hệ so sánh)
    # Equal to (==)
print('5 == 5', 5 == 5)         # True
print('6 == 5', 6 == 5)         # False
    # Not equal to (!=)
print('5 != 5', 5 != 5)         # False
print('6 != 5', 6 != 5)         # True
    # Greater/Less than (>)
print('5 > 5', 5 > 5)           # False
print('4 < 5', 4 < 5)           # True
    # Greater/Less than or equal to (>=, <=)
print('5 >= 5', 5 >= 5)         # True
print('4 <= 5', 4 <= 5)         # True

