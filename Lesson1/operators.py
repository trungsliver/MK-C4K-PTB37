# Basic: + - * /
    # Plus: +
print('1 + 1 =', 1+1)

    # Minus: -
print('7 - 2 =', 7-2)

    # Multiply: *
print('3 * 4 =', 3*4)

    # Divide: /
print('10 / 2 =', 10/2)

# Advanced: // % **
    # Floor Division (chia lấy nguyên): //
print('7 // 3 =', 7//3)

    # Modulo (chia lấy dư): %
print('7 % 3 =', 7%3)

    # Exponentiation (lũy thừa): **
print('2 ** 3 =', 2**3)

    # square root (căn bậc 2)
print('Square root of 16 =', 16**0.5)

import math 
print('Square root of 25 =', math.sqrt(25))

# Test
print('PI =', math.pi)

    # round (làm tròn)
print('PI =', round(math.pi, 2))
